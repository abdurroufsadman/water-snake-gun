name ="harry"

print(name[0:3])# it will print from 0 to 2 (excluding 3)
print(name[-4:-1])# it will print from -4 to -2 (excluding -1)
print(name[1:4])# it will print from 1 to 3 (excluding 4) .itis the same as the negative slicing DONE IN THE LINE ABOVE
print(name[:4])# same as name[0:4]
print(name[1:])# same as name[1:5]  it will print from 1 to 4 (excluding 5)
print(name[:])# it will print the whole string     same as name[0:5]



